package com.mycompany.main.models;

/**
 *
 * @author marce
 */
public class Book {

    // Encapsulation
    // Encapsulation protects an object's internal state and allows access only through controlled methods
    private String title;
    private boolean available;

     // Constructor
    public Book(String title) {
        this.title = title;
        this.available = true;
    }

    public String getTitle() {
        return title;
    }

    public boolean isAvailable() {
        return available;
    }

    public void borrow() {
        available = false;
    }

    public void returnBook() {
        available = true;
    }
}
